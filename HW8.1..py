mood = input("What is your mood today? ")

if mood == "happy":
    print("It's great to see you happy!")
elif mood == "nervous":
    print("Relax, take three deep breaths.")
elif mood == "sad":
    print("Do you want to talk about it?")
elif mood == "excited":
    print("Great! But why?")
elif mood == "relaxed":
    print("Well, at least one of us is.")
else:
    print("I don't recognize this mood.")